import { describe, it, expect, afterEach } from 'vitest';
import { access, mkdir, readFile, rm, writeFile } from 'node:fs/promises';
import { join } from 'node:path';
import { pathToFileURL } from 'node:url';
import { loadServerTypesText } from './loadServerTypesText';
import type { TypesSourceData } from './loadServerTypesText';
import { normalizeTypesSourceDataForCache } from './normalizeTypesSourceDataForCache';
import { buildTypesTextCacheContent } from './resolveTypesCacheKey';
import { hashCacheContent, resolveCachePath, saveFileCache } from '../cacheUtils';
import { generateTypesMarkdown } from '../syncTypes/generateTypesMarkdown';
import { parseMarkdownToHast } from '../loadServerTypesMeta/format';

const TEST_DIR = join(__dirname, '.test-loadServerTypesText');
const CACHE_DIR = join(TEST_DIR, '.cache');
const TYPES_PATH = join(TEST_DIR, 'src', 'app', 'components', 'button', 'types.md');

const TYPES_MARKDOWN = `# Button API

[//]: types.ts '<-- Autogenerated By (do not edit the following markdown directly)'

## API Reference

### Button
`;

const cacheRef = { cacheDir: CACHE_DIR, namespace: 'types-text', cacheKey: 'components/button' };

function fakeData(typeNameMap: Record<string, string>): TypesSourceData {
  return {
    exports: {},
    additionalTypes: [],
    variantOnlyAdditionalTypes: {},
    variantTypeNames: {},
    variantTypeNameMaps: {},
    externalTypes: {},
    typeNameMap,
    allDependencies: [TYPES_PATH],
    updated: false,
  };
}

async function writeTypesFile() {
  await mkdir(join(TEST_DIR, 'src', 'app', 'components', 'button'), { recursive: true });
  await writeFile(TYPES_PATH, TYPES_MARKDOWN, 'utf-8');
}

describe('loadServerTypesText caching', () => {
  afterEach(async () => {
    await rm(TEST_DIR, { recursive: true, force: true });
  });

  it('returns the cached parse without re-parsing when the content matches', async () => {
    await writeTypesFile();
    await saveFileCache(
      cacheRef,
      buildTypesTextCacheContent(TYPES_MARKDOWN),
      fakeData({ CACHED: 'yes' }),
    );

    const result = await loadServerTypesText(pathToFileURL(TYPES_PATH).href, undefined, {
      cacheDir: CACHE_DIR,
      rootContext: TEST_DIR,
    });
    // The sentinel could never come from parsing the markdown, proving the parse was skipped.
    expect(result.typeNameMap).toEqual({ CACHED: 'yes' });
  });

  it('parses on a miss and writes the cache with the content hash', async () => {
    await writeTypesFile();
    const result = await loadServerTypesText(pathToFileURL(TYPES_PATH).href, undefined, {
      cacheDir: CACHE_DIR,
      rootContext: TEST_DIR,
    });
    expect(result.exports.Button).toBeDefined();

    const raw = await readFile(resolveCachePath(cacheRef), 'utf-8');
    expect(JSON.parse(raw).hash).toBe(hashCacheContent(buildTypesTextCacheContent(TYPES_MARKDOWN)));
  });

  it('ignores a stale cache (hash mismatch) and re-parses', async () => {
    await writeTypesFile();
    // Hash computed from different content, so it will not match the file.
    await saveFileCache(
      cacheRef,
      buildTypesTextCacheContent('different'),
      fakeData({ STALE: 'yes' }),
    );

    const result = await loadServerTypesText(pathToFileURL(TYPES_PATH).href, undefined, {
      cacheDir: CACHE_DIR,
      rootContext: TEST_DIR,
    });
    expect(result.exports.Button).toBeDefined();
    expect(result.typeNameMap).not.toHaveProperty('STALE');
  });

  it('does not read or write a cache when cacheDir is unset', async () => {
    await writeTypesFile();
    const result = await loadServerTypesText(pathToFileURL(TYPES_PATH).href);
    expect(result.exports.Button).toBeDefined();
    await expect(access(CACHE_DIR)).rejects.toThrow();
  });

  it('invalidates the cache when the ordering changes', async () => {
    await writeTypesFile();
    // Seed a cache entry keyed by the content with no ordering.
    await saveFileCache(
      cacheRef,
      buildTypesTextCacheContent(TYPES_MARKDOWN),
      fakeData({ NO_ORDER: 'yes' }),
    );

    // Reading with a different ordering changes the hash, so the seeded entry is ignored.
    const result = await loadServerTypesText(
      pathToFileURL(TYPES_PATH).href,
      { props: ['a'] },
      {
        cacheDir: CACHE_DIR,
        rootContext: TEST_DIR,
      },
    );
    expect(result.exports.Button).toBeDefined();
    expect(result.typeNameMap).not.toHaveProperty('NO_ORDER');
  });

  it('normalizes source-derived data to match a cached cold parse byte-for-byte', async () => {
    const sourceData: TypesSourceData = {
      exports: {
        Button: {
          type: {
            type: 'component',
            name: 'Button',
            data: {
              name: 'Button',
              description: await parseMarkdownToHast('A clickable button component'),
              descriptionText: 'A clickable button component',
              props: {},
              dataAttributes: {},
              cssVariables: {},
            },
          },
          additionalTypes: [],
        },
      },
      additionalTypes: [],
      variantOnlyAdditionalTypes: {},
      externalTypes: {},
      typeNameMap: {},
      variantTypeNameMaps: {},
      variantTypeNames: { Default: ['Button'] },
      allDependencies: [TYPES_PATH],
      updated: false,
    };

    const markdown = await generateTypesMarkdown({
      name: 'Button API',
      organized: {
        exports: sourceData.exports,
        additionalTypes: sourceData.additionalTypes,
        variantOnlyAdditionalTypes: sourceData.variantOnlyAdditionalTypes,
        variantTypeNameMaps: sourceData.variantTypeNameMaps,
        variantTypeNames: sourceData.variantTypeNames,
      },
      typeNameMap: sourceData.typeNameMap,
      externalTypes: sourceData.externalTypes,
    });
    await mkdir(join(TEST_DIR, 'src', 'app', 'components', 'button'), { recursive: true });
    await writeFile(TYPES_PATH, markdown, 'utf-8');

    const coldParsed = await loadServerTypesText(pathToFileURL(TYPES_PATH).href);
    expect(JSON.stringify(normalizeTypesSourceDataForCache(sourceData))).toBe(
      JSON.stringify(coldParsed),
    );
  });
});
