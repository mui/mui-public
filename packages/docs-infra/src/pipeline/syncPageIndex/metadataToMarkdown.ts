/**
 * Serializes a URL for a markdown link destination. A URL containing parentheses (such as a
 * Next.js route group like `./(handbook)/styling/page.mdx`) or whitespace is wrapped in angle
 * brackets, matching how prettier normalizes it — so the generated file stays stable when
 * prettier formats it afterwards. The mdast parser reads the angle-bracketed form back to the
 * same URL, so the round-trip is unaffected.
 */
export function formatLinkUrl(url: string): string {
  return /[\s()]/.test(url) ? `<${url}>` : url;
}

/**
 * Converts AST nodes (from heading.children) back to markdown string
 */
function astNodesToMarkdown(nodes: any[]): string {
  let result = '';
  for (const node of nodes) {
    if (node.type === 'text') {
      result += node.value;
    } else if (node.type === 'inlineCode') {
      result += `\`${node.value}\``;
    } else if (node.type === 'emphasis') {
      // Prettier serializes emphasis with underscores; match it to keep the
      // generated file stable when prettier formats it afterwards.
      result += `_${astNodesToMarkdown(node.children)}_`;
    } else if (node.type === 'strong') {
      result += `**${astNodesToMarkdown(node.children)}**`;
    } else if (node.type === 'link') {
      result += `[${astNodesToMarkdown(node.children)}](${formatLinkUrl(node.url)})`;
    } else if ('children' in node) {
      result += astNodesToMarkdown(node.children);
    }
  }
  return result;
}

export interface PageMetadata extends ExtractedMetadata {
  /** The slug/path for this page (e.g., 'button', 'checkbox') */
  slug: string;
  /** The relative path to the page's MDX file */
  path: string;
  /**
   * User-customized display title for the page.
   * When set, this overrides `title` in the navigation list and sitemap.
   * The `title` field continues to be used for the heading section.
   *
   * This value is detected automatically: if a user edits the list item text
   * so it no longer matches the heading, the edited text is preserved here.
   */
  displayTitle?: string;
  /** Tags for this entry (e.g., 'New', 'Hot', 'Beta') */
  tags?: string[];
  /** Skip generating detail section for this entry (for external links) */
  skipDetailSection?: boolean;
  /**
   * The route group of the index section this page is manually filed under, in a grouped
   * index. Only meaningful for a page whose own path has no route group (e.g. an external
   * link like `/llms.txt`): it can't derive a section from its path, so this remembers the
   * header a human placed it under and keeps it there across regenerations. Route-grouped
   * pages ignore this — they always sort into the section matching their path's group.
   */
  sectionGroup?: string;
  /**
   * The intended audience for this page.
   * When omitted, the page is public and intended for all audiences.
   */
  audience?: Audience;
  /** Whether this page is an index page (has the autogenerated comment marker) */
  index?: boolean;
  /** Component parts with their API metadata (for multi-part components) */
  parts?: Record<
    string,
    {
      props?: string[];
      dataAttributes?: string[];
      cssVariables?: string[];
      /** For hooks and functions: parameter names.
       * Each element is a string (positional param) or string[] (object param keys). */
      parameters?: (string | string[])[];
      /** For hooks and functions: return value keys (if returns an object) */
      returns?: string[];
    }
  >;
  /** Component exports with their API metadata (used for both single and multi-part components) */
  exports?: Record<
    string,
    {
      /** For components: prop names */
      props?: string[];
      /** For components: data attribute names */
      dataAttributes?: string[];
      /** For components: CSS variable names */
      cssVariables?: string[];
      /** For hooks and functions: parameter names.
       * Each element is a string (positional param) or string[] (object param keys). */
      parameters?: (string | string[])[];
      /** For hooks and functions: return value keys (if returns an object) */
      returns?: string[];
    }
  >;
  /** Type names documented on this page (state objects, enums, type aliases, etc.) */
  types?: string[];
}

