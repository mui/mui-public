import { describe, it, expect } from 'vitest';
import { metadataToMarkdown, markdownToMetadata } from './metadataToMarkdown';
import { mergeMetadataMarkdown } from './mergeMetadataMarkdown';
import type { PagesMetadata } from './metadataToMarkdown';

describe('group-aware index rendering', () => {
  it('renders route-group sections with a Details wrapper', () => {
    const data: PagesMetadata = {
      title: 'React',
      sections: [
        { group: '(overview)', title: 'Overview' },
        { group: '(utils)', title: 'Utilities' },
      ],
      pages: [
        {
          slug: 'quick-start',
          path: './(overview)/quick-start/page.mdx',
          title: 'Quick start',
          description: 'Get started.',
        },
        {
          slug: 'userender',
          path: './(utils)/use-render/page.mdx',
          title: 'useRender',
          description: 'Render prop helper.',
        },
      ],
    };

    const result = metadataToMarkdown(data, { path: 'src/app/react/page.mdx' });

    expect(result).toMatchInlineSnapshot(`
      "# React

      [//]: # 'This section is autogenerated, but the following list order, title, and [Tag]s can be modified, but nothing within the parentheses.'

      ## Overview

      - Quick start - ([Outline](#quick-start), [Contents](./(overview)/quick-start/page.mdx))

      ## Utilities

      - useRender - ([Outline](#userender), [Contents](./(utils)/use-render/page.mdx))

      [//]: # 'This section is autogenerated, DO NOT EDIT AFTER THIS LINE, run: pnpm docs:validate react'

      ## Details

      ### Quick start

      Get started.

      [Read more](./(overview)/quick-start/page.mdx)

      ### useRender

      Render prop helper.

      [Read more](./(utils)/use-render/page.mdx)

      [//]: # 'The above section is autogenerated, but the remainder of the file can be modified.'

      export const metadata =
        /** @type {import('@mui/internal-docs-infra/createSitemap/types').NextMetadata} */ ({
          robots: {
            index: false,
          },
          other: {
            audience: 'private',
          },
        });
      "
    `);
  });

  it('round-trips: parse recovers sections/detailsSectionTitle and re-render is stable', async () => {
    const data: PagesMetadata = {
      title: 'React',
      sections: [
        { group: '(overview)', title: 'Overview' },
        { group: '(utils)', title: 'Utilities' },
      ],
      pages: [
        {
          slug: 'quick-start',
          path: './(overview)/quick-start/page.mdx',
          title: 'Quick start',
          description: 'Get started.',
        },
        {
          slug: 'userender',
          path: './(utils)/use-render/page.mdx',
          title: 'useRender',
          description: 'Render prop helper.',
        },
      ],
    };

    const markdown = metadataToMarkdown(data, { path: 'src/app/react/page.mdx' });
    const parsed = await markdownToMetadata(markdown);

    expect(parsed?.sections).toEqual([
      { group: '(overview)', title: 'Overview' },
      { group: '(utils)', title: 'Utilities' },
    ]);
    expect(parsed?.detailsSectionTitle).toBe('Details');
    expect(parsed?.pages.map((page) => page.path)).toEqual([
      './(overview)/quick-start/page.mdx',
      './(utils)/use-render/page.mdx',
    ]);

    // Re-rendering the parsed metadata reproduces the same markdown.
    const reserialized = metadataToMarkdown(parsed!, { path: 'src/app/react/page.mdx' });
    expect(reserialized).toBe(markdown);
  });

  it('preserves a renamed section heading and routes a new page under it', async () => {
    // Human renamed "## Utils" to "## Utilities"; regeneration keeps the rename and
    // places a newly-added (utils) page under it (matched by route group, not heading text).
    const existing = metadataToMarkdown(
      {
        title: 'React',
        sections: [{ group: '(utils)', title: 'Utilities' }],
        pages: [
          {
            slug: 'userender',
            path: './(utils)/use-render/page.mdx',
            title: 'useRender',
            description: 'Render prop helper.',
          },
        ],
      },
      { path: 'src/app/react/page.mdx' },
    );

    const parsed = await markdownToMetadata(existing);
    expect(parsed?.sections).toEqual([{ group: '(utils)', title: 'Utilities' }]);

    // A regeneration run adds a second (utils) page.
    const regenerated = metadataToMarkdown(
      {
        ...parsed!,
        pages: [
          ...parsed!.pages,
          {
            slug: 'merge-props',
            path: './(utils)/merge-props/page.mdx',
            title: 'mergeProps',
            description: 'Merge props helper.',
          },
        ],
      },
      { path: 'src/app/react/page.mdx' },
    );

    expect(regenerated).toContain('## Utilities');
    // Both (utils) pages sit under the single Utilities section.
    const utilitiesBlock = regenerated.slice(
      regenerated.indexOf('## Utilities'),
      regenerated.indexOf('[//]: #', regenerated.indexOf('## Utilities')),
    );
    expect(utilitiesBlock).toContain('- useRender -');
    expect(utilitiesBlock).toContain('- mergeProps -');
  });
});

describe('group-aware index merge', () => {
  it('auto-groups a fresh index by route group with seeded titles', async () => {
    const result = await mergeMetadataMarkdown(
      undefined,
      {
        title: 'React',
        pages: [
          {
            slug: 'accordion',
            path: './(components)/accordion/page.mdx',
            title: 'Accordion',
            description: 'An accordion.',
          },
          {
            slug: 'use-render',
            path: './(utils)/use-render/page.mdx',
            title: 'useRender',
            description: 'Render prop helper.',
          },
        ],
      },
      { path: 'src/app/react/page.mdx' },
    );

    // Sections are seeded from the route-group folder names, in page-encounter order,
    // with the detail region (### headings) after the editable section list.
    expect(result).toContain('## Components');
    expect(result).toContain('## Utils');
    expect(result.indexOf('## Components')).toBeLessThan(result.indexOf('## Utils'));
    expect(result.indexOf('## Utils')).toBeLessThan(result.indexOf('## Details'));
    expect(result.indexOf('## Details')).toBeLessThan(result.indexOf('### Accordion'));
    expect(result.indexOf('### Accordion')).toBeLessThan(result.indexOf('### useRender'));
  });

  it('routes a new page under its section and preserves a renamed heading (via merge)', async () => {
    const existing = metadataToMarkdown(
      {
        title: 'React',
        sections: [{ group: '(utils)', title: 'Utilities' }],
        pages: [
          {
            slug: 'use-render',
            path: './(utils)/use-render/page.mdx',
            title: 'useRender',
            description: 'Render prop helper.',
          },
        ],
      },
      { path: 'src/app/react/page.mdx' },
    );

    const merged = await mergeMetadataMarkdown(
      existing,
      {
        title: 'React',
        pages: [
          {
            slug: 'use-render',
            path: './(utils)/use-render/page.mdx',
            title: 'useRender',
            description: 'Render prop helper.',
          },
          {
            slug: 'merge-props',
            path: './(utils)/merge-props/page.mdx',
            title: 'mergeProps',
            description: 'Merge props helper.',
          },
        ],
      },
      { path: 'src/app/react/page.mdx' },
    );

    expect(merged).toContain('## Utilities');
    expect(merged).not.toContain('## Utils\n');
    const utilitiesBlock = merged.slice(
      merged.indexOf('## Utilities'),
      merged.indexOf('[//]: #', merged.indexOf('## Utilities')),
    );
    expect(utilitiesBlock).toContain('- useRender -');
    // The new page is tagged [New] and placed under the same section.
    expect(utilitiesBlock).toContain('- mergeProps [New] -');
  });

  it('removes a deleted page and drops an emptied section without disturbing others', async () => {
    const existing = metadataToMarkdown(
      {
        title: 'React',
        sections: [
          { group: '(overview)', title: 'Overview' },
          { group: '(utils)', title: 'Utilities' },
        ],
        pages: [
          {
            slug: 'quick-start',
            path: './(overview)/quick-start/page.mdx',
            title: 'Quick start',
            description: 'Get started.',
          },
          {
            slug: 'use-render',
            path: './(utils)/use-render/page.mdx',
            title: 'useRender',
            description: 'Render prop helper.',
          },
        ],
      },
      { path: 'src/app/react/page.mdx' },
    );

    // Regeneration no longer lists the (utils) page.
    const merged = await mergeMetadataMarkdown(
      existing,
      {
        title: 'React',
        pages: [
          {
            slug: 'quick-start',
            path: './(overview)/quick-start/page.mdx',
            title: 'Quick start',
            description: 'Get started.',
          },
        ],
      },
      { path: 'src/app/react/page.mdx' },
    );

    expect(merged).toContain('## Overview');
    expect(merged).toContain('- Quick start -');
    // The emptied Utilities section and its page are gone.
    expect(merged).not.toContain('## Utilities');
    expect(merged).not.toContain('useRender');
  });

  it('groups consecutive route groups under the outermost group', async () => {
    // Pages that bubbled past two consecutive route groups: the outermost is the section,
    // the inner one is transparent. Two pages sharing the outermost group land together.
    const result = await mergeMetadataMarkdown(
      undefined,
      {
        title: 'React',
        pages: [
          {
            slug: 'checkbox',
            path: './(components)/(inputs)/checkbox/page.mdx',
            title: 'Checkbox',
            description: 'A checkbox.',
          },
          {
            slug: 'avatar',
            path: './(components)/(display)/avatar/page.mdx',
            title: 'Avatar',
            description: 'An avatar.',
          },
        ],
      },
      { path: 'src/app/react/page.mdx' },
    );

    // A single "## Components" section (from the outermost group), not one per inner group.
    expect(result).toContain('## Components');
    expect(result).not.toContain('## Inputs');
    expect(result).not.toContain('## Display');
    const componentsBlock = result.slice(
      result.indexOf('## Components'),
      result.indexOf('[//]: #', result.indexOf('## Components')),
    );
    expect(componentsBlock).toContain('- Checkbox -');
    expect(componentsBlock).toContain('- Avatar -');
    // Links retain the full route-group path (stripped to the URL only at render time).
    expect(result).toContain('[Contents](./(components)/(inputs)/checkbox/page.mdx)');
  });

  it('treats dynamic segments as non-group segments', async () => {
    // Dynamic segments ([slug], [...slug]) are not route groups: a page directly under
    // one is ungrouped, while a route group with a dynamic segment beneath it still groups.
    const result = await mergeMetadataMarkdown(
      undefined,
      {
        title: 'React',
        pages: [
          {
            slug: 'accordion',
            path: './(components)/[variant]/accordion/page.mdx',
            title: 'Accordion',
            description: 'An accordion.',
          },
          {
            slug: 'catch-all',
            path: './[...slug]/page.mdx',
            title: 'Catch All',
            description: 'A dynamic page.',
          },
        ],
      },
      { path: 'src/app/react/page.mdx' },
    );

    // The route-group page groups (dynamic child is irrelevant to sectioning)...
    expect(result).toContain('## Components');
    const componentsBlock = result.slice(
      result.indexOf('## Components'),
      result.indexOf('[//]: #', result.indexOf('## Components')),
    );
    expect(componentsBlock).toContain('- Accordion -');
    // ...and the dynamic-segment page is ungrouped (listed before the first section, no heading).
    expect(result).not.toContain('## Slug');
    expect(result.indexOf('- Catch All')).toBeLessThan(result.indexOf('## Components'));
  });
});

describe('group-aware index parser edge cases', () => {
  const EDITABLE_MARKER =
    "[//]: # 'This section is autogenerated, but the following list order, title, and [Tag]s can be modified, but nothing within the parentheses.'";
  const DO_NOT_EDIT_MARKER = "[//]: # 'This section is autogenerated, DO NOT EDIT AFTER THIS LINE'";

  it('does not let an empty/placeholder heading steal the next section group or duplicate pages', async () => {
    // A human-added heading with no pages of its own must not adopt the following
    // section's route group (which would render every page of that group twice).
    const markdown = [
      '# React',
      '',
      EDITABLE_MARKER,
      '',
      '## Placeholder',
      '',
      '## Utilities',
      '',
      '- useRender - ([Outline](#userender), [Contents](./(utils)/use-render/page.mdx))',
      '',
      DO_NOT_EDIT_MARKER,
      '',
      '## Details',
      '',
      '### useRender',
      '',
      'Render prop helper.',
      '',
      '[Read more](./(utils)/use-render/page.mdx)',
      '',
    ].join('\n');

    const parsed = await markdownToMetadata(markdown);

    // Placeholder is dropped (no route group); only the real (utils) section survives.
    expect(parsed?.sections).toEqual([{ group: '(utils)', title: 'Utilities' }]);
    expect(parsed?.pages).toHaveLength(1);

    // Re-rendering lists the page exactly once (no duplicate under two headings).
    const rerendered = metadataToMarkdown(parsed!, { path: 'src/app/react/page.mdx' });
    expect(rerendered.split('- useRender -')).toHaveLength(2);
    expect(rerendered.split('### useRender')).toHaveLength(2);
  });

  it('infers the section group from the first grouped page even if an earlier item is ungrouped', async () => {
    const markdown = [
      '# React',
      '',
      EDITABLE_MARKER,
      '',
      '## Utilities',
      '',
      '- [llms.txt](/llms.txt) [External]',
      '- useRender - ([Outline](#userender), [Contents](./(utils)/use-render/page.mdx))',
      '',
      DO_NOT_EDIT_MARKER,
      '',
      '## Details',
      '',
      '### useRender',
      '',
      'Render prop helper.',
      '',
    ].join('\n');

    const parsed = await markdownToMetadata(markdown);
    // The heading keeps its group despite the first list item being an ungrouped external link.
    expect(parsed?.sections).toEqual([{ group: '(utils)', title: 'Utilities' }]);
  });

  it('round-trips a non-default section heading depth', async () => {
    const markdown = metadataToMarkdown(
      {
        title: 'React',
        sections: [{ group: '(utils)', title: 'Utilities', depth: 3 }],
        pages: [
          {
            slug: 'use-render',
            path: './(utils)/use-render/page.mdx',
            title: 'useRender',
            description: 'Render prop helper.',
          },
        ],
      },
      { path: 'src/app/react/page.mdx' },
    );

    expect(markdown).toContain('### Utilities');
    const parsed = await markdownToMetadata(markdown);
    expect(parsed?.sections).toEqual([{ group: '(utils)', title: 'Utilities', depth: 3 }]);
  });

  it('treats only the first detail-region H2 as the Details wrapper', async () => {
    // A stray H2 inside the detail region must not overwrite the wrapper title or discard
    // the in-progress page's parsed metadata.
    const markdown = [
      '# React',
      '',
      EDITABLE_MARKER,
      '',
      '## Utilities',
      '',
      '- useRender - ([Outline](#userender), [Contents](./(utils)/use-render/page.mdx))',
      '',
      DO_NOT_EDIT_MARKER,
      '',
      '## Details',
      '',
      '### useRender',
      '',
      'Render prop helper.',
      '',
      '## Stray note',
      '',
      'Some prose a human added.',
      '',
    ].join('\n');

    const parsed = await markdownToMetadata(markdown);
    // The wrapper title is still "Details", and the page kept its description.
    expect(parsed?.detailsSectionTitle).toBe('Details');
    expect(parsed?.pages[0]?.description).toBe('Render prop helper.');
  });
});
