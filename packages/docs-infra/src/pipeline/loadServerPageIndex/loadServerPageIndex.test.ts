import { describe, it, expect, afterEach } from 'vitest';
import { access, mkdir, readFile, rm, writeFile } from 'node:fs/promises';
import { join } from 'node:path';
import { createLoadServerPageIndex } from './loadServerPageIndex';
import { hashCacheContent, resolveCachePath, saveFileCache } from '../cacheUtils';
import type { SitemapSectionData } from '../../createSitemap/types';

const TEST_DIR = join(__dirname, '.test-loadServerPageIndex');
const CACHE_DIR = join(TEST_DIR, '.cache');
const INDEX_PATH = join(TEST_DIR, 'src', 'app', 'components', 'page.mdx');

const INDEX_MARKDOWN = `# Components

[//]: # 'This section is autogenerated, but the following list order, title, and [Tag]s can be modified, but nothing within the parentheses.'

- Button - ([Outline](#button), [Contents](./button/page.mdx))

[//]: # 'This section is autogenerated, DO NOT EDIT AFTER THIS LINE'

## Button

A button component.

[Read more](./button/page.mdx)

[//]: # 'The above section is autogenerated, but the remainder of the file can be modified.'
`;

const cacheRef = { cacheDir: CACHE_DIR, namespace: 'pages-index', cacheKey: 'components' };

async function writeIndexFile() {
  await mkdir(join(TEST_DIR, 'src', 'app', 'components'), { recursive: true });
  await writeFile(INDEX_PATH, INDEX_MARKDOWN, 'utf-8');
}

describe('createLoadServerPageIndex caching', () => {
  afterEach(async () => {
    await rm(TEST_DIR, { recursive: true, force: true });
  });

  it('returns the cached value without parsing when the hash matches', async () => {
    await writeIndexFile();
    // Seed the cache with a sentinel that the markdown could never produce.
    const sentinel: SitemapSectionData = {
      title: 'CACHED',
      prefix: '/cached/',
      pages: [],
      sections: [],
    };
    await saveFileCache(cacheRef, INDEX_MARKDOWN, sentinel);

    const load = createLoadServerPageIndex({ rootContext: TEST_DIR, cacheDir: CACHE_DIR });
    expect(await load(INDEX_PATH)).toEqual(sentinel);
  });

  it('parses on a miss and writes the cache for next time', async () => {
    await writeIndexFile();
    const load = createLoadServerPageIndex({ rootContext: TEST_DIR, cacheDir: CACHE_DIR });

    const result = await load(INDEX_PATH);
    expect(result?.title).toBe('Components');
    expect(result?.prefix).toBe('/components/');
    expect(result?.pages).toHaveLength(1);

    const raw = await readFile(resolveCachePath(cacheRef), 'utf-8');
    expect(JSON.parse(raw)).toEqual({ hash: hashCacheContent(INDEX_MARKDOWN), data: result });
  });

  it('ignores a stale cache (hash mismatch) and overwrites it', async () => {
    await writeIndexFile();
    const sentinel: SitemapSectionData = {
      title: 'STALE',
      prefix: '/stale/',
      pages: [],
      sections: [],
    };
    // Hash computed from different content, so it will not match the file.
    await saveFileCache(cacheRef, 'different content', sentinel);

    const load = createLoadServerPageIndex({ rootContext: TEST_DIR, cacheDir: CACHE_DIR });
    const result = await load(INDEX_PATH);
    expect(result?.title).toBe('Components');

    const raw = await readFile(resolveCachePath(cacheRef), 'utf-8');
    expect(JSON.parse(raw).hash).toBe(hashCacheContent(INDEX_MARKDOWN));
  });

  it('does not read or write a cache when cacheDir is unset', async () => {
    await writeIndexFile();
    const load = createLoadServerPageIndex({ rootContext: TEST_DIR });

    const result = await load(INDEX_PATH);
    expect(result?.title).toBe('Components');
    await expect(access(CACHE_DIR)).rejects.toThrow();
  });
});
