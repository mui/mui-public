import { describe, it, expect, afterEach } from 'vitest';
import { mkdir, readFile, rm, writeFile } from 'node:fs/promises';
import { join } from 'node:path';
import { loadServerTypes } from './loadServerTypes';
import type { LoadServerTypesOptions } from './loadServerTypes';
import { resolveCachePath } from '../cacheUtils';

const TEST_DIR = join(__dirname, '.test-loadServerTypes-cache');
const CACHE_DIR = join(TEST_DIR, '.cache');
const TYPES_PATH = join(TEST_DIR, 'src', 'app', 'components', 'button', 'types.md');

function typesMarkdown(componentName: string): string {
  return `# ${componentName} API

[//]: types.ts '<-- Autogenerated By (do not edit the following markdown directly)'

## API Reference

### ${componentName}
`;
}

const enhancedRef = {
  cacheDir: CACHE_DIR,
  namespace: 'types-enhanced',
  cacheKey: 'components/button',
};

async function writeTypes(markdown: string) {
  await mkdir(join(TEST_DIR, 'src', 'app', 'components', 'button'), { recursive: true });
  await writeFile(TYPES_PATH, markdown, 'utf-8');
}

function options(overrides?: Partial<LoadServerTypesOptions>): LoadServerTypesOptions {
  return {
    typesMarkdownPath: TYPES_PATH,
    rootContext: TEST_DIR,
    sync: false,
    cacheDir: CACHE_DIR,
    ...overrides,
  };
}

// Replaces the cached data with a sentinel the pipeline could never produce, keeping the
// validating hash, so a subsequent cache HIT is observable and a MISS is observable.
async function sentinelTheCache() {
  const cachePath = resolveCachePath(enhancedRef);
  const entry = JSON.parse(await readFile(cachePath, 'utf-8'));
  entry.data = { ...entry.data, anchorMap: { js: { SENTINEL: '#sentinel' } } };
  await writeFile(cachePath, JSON.stringify(entry), 'utf-8');
}

const SENTINEL = { SENTINEL: '#sentinel' };

describe('loadServerTypes post-processing cache', () => {
  afterEach(async () => {
    await rm(TEST_DIR, { recursive: true, force: true });
  });

  it('serves the result from cache on the second call (skipping highlighting)', async () => {
    await writeTypes(typesMarkdown('Button'));

    const first = await loadServerTypes(options());
    expect(first.exports.Button).toBeDefined();

    await sentinelTheCache();

    const second = await loadServerTypes(options());
    expect(second.anchorMap.js).toEqual(SENTINEL);
  });

  it('re-processes (cache miss) when the types.md content changes', async () => {
    await writeTypes(typesMarkdown('Button'));
    await loadServerTypes(options());
    await sentinelTheCache();

    // A different component changes the parsed data, so the hash no longer matches.
    await writeTypes(typesMarkdown('Checkbox'));
    const second = await loadServerTypes(options());

    expect(second.anchorMap.js).not.toEqual(SENTINEL);
    expect(second.exports.Checkbox).toBeDefined();
    expect(second.exports.Button).toBeUndefined();
  });

  it('re-processes (cache miss) when the output format changes', async () => {
    await writeTypes(typesMarkdown('Button'));
    await loadServerTypes(options({ output: 'hast' }));
    await sentinelTheCache();

    // The output option is part of the cache hash, so a different format misses.
    const second = await loadServerTypes(options({ output: 'hastCompressed' }));

    expect(second.anchorMap.js).not.toEqual(SENTINEL);
    expect(second.exports.Button).toBeDefined();
  });

  it('re-processes (cache miss) when formattingOptions change', async () => {
    await writeTypes(typesMarkdown('Button'));
    await loadServerTypes(options()); // formattingOptions undefined
    await sentinelTheCache();

    const second = await loadServerTypes(options({ formattingOptions: {} }));
    expect(second.anchorMap.js).not.toEqual(SENTINEL);
    expect(second.exports.Button).toBeDefined();
  });

  it('re-processes (cache miss) when codeBlockEmphasisOptions change', async () => {
    await writeTypes(typesMarkdown('Button'));
    await loadServerTypes(options()); // codeBlockEmphasisOptions undefined
    await sentinelTheCache();

    const second = await loadServerTypes(options({ codeBlockEmphasisOptions: {} }));
    expect(second.anchorMap.js).not.toEqual(SENTINEL);
    expect(second.exports.Button).toBeDefined();
  });

  it('does not write a cache when cacheDir is unset', async () => {
    await writeTypes(typesMarkdown('Button'));
    const result = await loadServerTypes(options({ cacheDir: undefined }));
    expect(result.exports.Button).toBeDefined();
    await expect(readFile(resolveCachePath(enhancedRef), 'utf-8')).rejects.toThrow();
  });
});
